  getResults(): Result[] {
    return this.results;
  }

  getResultsByManagerId(id: number): Result[] {
    return this.results.filter(x => x.winnerId == id || x.loserId == id);
  }

  getResultBySeasonWeekAndManager(id: number, season: number, week: number): Result {
    return this.results.filter(x => x.season == season && x.weekNumber == week && (x.winnerId == id || x.loserId == id))[0];
  }

  getTotalPointsAgainst(id: number, season: number): number {
    var total = 0;
    var losses = this.results.filter(x => x.loserId == id && x.season == season);
    var wins = this.results.filter(x => x.winnerId == id && x.season == season);
    wins.forEach(res => {
      total += res.losingScore;
    })
    losses.forEach(res => {
      total += res.winningScore;
    })
    return total;
  }

